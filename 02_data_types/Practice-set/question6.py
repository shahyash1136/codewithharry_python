# Write a program that:
# Takes two numbers as input from the user.
# Prints their sum, difference, product, and quotient

num1 = int(input("Enter number 1: "))
num2 = int(input("Enter number 2: "))

print(num1 + num2)
print(num1 - num2)
print(num1 * num2)
