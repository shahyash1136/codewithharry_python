# 2. String Slicing and Indexing
# 1. Given  text = "Python Programming" , do the following:
#     a. Print the first 6 characters
#     b. Print the last 6 characters
#     c. Print every second character from the string
text = "Python Programming"
print(text[:6])
print(text[-6:])
print(text[::2])

# 2. Reverse the string  text  using slicing.
print(text[::-1])
