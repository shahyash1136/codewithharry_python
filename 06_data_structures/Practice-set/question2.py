# 2. List Methods
# 1. Start with  numbers = [5, 2, 9, 1, 7]  and do the following:
#     a. Sort the list in ascending order.
#     b. Append the number  10  to the list.
#     c. Remove the number  2  from the list.
numbers = [5, 2, 9, 1, 7]
numbers.sort()
print(numbers)
numbers.append(10)
print(numbers)
numbers.remove(2)
print(numbers)

# 2. Create a list  names = ["Alice", "Bob", "Charlie"]  and use the  insert() method to add  "David"  at index  1
names = ["Alice", "Bob", "Charlie"]
names.insert(1, "David")
print(names)
